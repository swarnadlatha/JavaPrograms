//Multiplication of givem number 30-50 without parameter & without return type with in the class(method)
class Logic6
{
	static void mul()
	{
		int mul=1;
		for(int i=30;i<50;i++)
		{
			mul=mul*i;
		}
	System.out.println("the multiplication of given number" +mul);
	}

public static void main(String[] args) 
	{
		mul();
	}
}
