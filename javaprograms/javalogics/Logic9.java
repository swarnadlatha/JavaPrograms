//Count of given number 30-50 without parameter & without return type with in the class(method)
class Logic9
{
	static void count()
	{
    int count=0;
	for(int i=30;i<=50;i++)
		{
		count++;
		}

	System.out.println("the count of odd number" +count++);
	}

	public static void main(String[] args) 
	{
		count();
	}
}
